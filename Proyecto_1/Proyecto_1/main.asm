	BRLO	FIN_DEC_MIN
	SUBI	R16, 10
	INC		R17
	RJMP	DEC_MIN

FIN_DEC_MIN:
	MOV		R18, R16
	MOV		R16, R17

	CALL	NUM_A_7SEG
	STS		DISP2, R16

	MOV		R16, R18
	CALL	NUM_A_7SEG
	STS		DISP3, R16
	
	RET

