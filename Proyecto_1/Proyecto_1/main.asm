//----------MODOS-------------------/
.equ	MODE_CLOCK		= 0
.equ	MODE_SET_CLOCK	= 1
.equ	MODE_DATE		= 2
.equ	MODE_SET_DATE	= 3
.equ	MODE_SET_ALARM	= 4
.equ	MODE_RING		= 5
.equ	MAX_MODES		= 5

//----------BANDERAS---------------/
.equ	FLAG_PCINT		= 0
.equ	FLAG_TIMER_1	= 1

//----------DATOS-----------------/
.equ	T0VALUE			= 0x7C // Prescaler de 8
.equ	T1VALUE			= 0x3D08 // Prescaler de 64

//----------PATRONES DE SEGMENTOS-----------------/
.equ	SEG_C		= 0b0111001
.equ	SEG_L		= 0b0111000
.equ	SEG_O		= 0b0111111
.equ	SEG_d		= 0b1011110
.equ	SEG_A		= 0b1110111
.equ	SEG_t		= 0b1111000
.equ	SEG_E		= 0b1111001
.equ	SEG_S		= 0b1101101
.equ	SEG_r		= 0b1010000
.equ	SEG_I		= 0b0000110
.equ	SEG_n		= 0b1010100
.equ	SEG_G		= 0b0111101

.equ	SEG_0		= 0b0111111
.equ	SEG_1		= 0b0000110
.equ	SEG_2		= 0b1011011
.equ	SEG_3		= 0b1001111
.equ	SEG_4		= 0b1100110
.equ	SEG_5		= 0b1101101
.equ	SEG_6		= 0b1111101
.equ	SEG_7		= 0b0000111
.equ	SEG_8		= 0b1111111
.equ	SEG_9		= 0b1101111

//----------REGISTROS-----------------/
.def	MODE			= R20
.def	ACTION			= R21

.dseg
.org    SRAM_START

// -----------DISPLAY------------/
DISP0:		.byte 1
DISP1:		.byte 1
DISP2:		.byte 1
DISP3:		.byte 1

MUX:		.byte 1 // 0 - 3

MENSAJE:	.byte 1 // Mensaje de modo
MEN_LOW:	.byte 1
MEN_HIGH:	.byte 1

PREV_D7:	.byte 1 // Guarda el estado anterior
PUNTO_CONT:	.byte 1 // contador para parpadeo de :
PUNTO_HALF:	.byte 1

SEGUNDOS:	.byte 1
MINUTOS:	.byte 1
HORAS:		.byte 1

DIA:		.byte 1
MES:		.byte 1

AL_HORAS:	.byte 1
AL_MINUTOS:	.byte 1
AL_ACTIVA:	.byte 1

//variable_name:     .byte   1   // Memory alocation for variable_name:     .byte   (byte size)

.cseg
.org 0x0000

	JMP		START
.org	PCI1addr	// Interrupciones para puerto C
	JMP		PINC_ISR

.org	PCI2addr	// Interrupciones para puerto D
	JMP		PIND_ISR

.org	OC1Aaddr	// Interrupciones para Timer 1 Compare match A
	JMP		TIMER1_ISR

.org	OC0Aaddr	// Interrupciones para Timer 0Compare match A
	JMP		TIMER0_ISR

START:
 /****************************************/
// Configuración de la pila
LDI     R16, LOW(RAMEND)
OUT     SPL, R16
LDI     R16, HIGH(RAMEND)
OUT     SPH, R16
/****************************************/
// Configuracion MCU
SETUP:

	// Configuramos el reloj a 1 MHz
    CLI
	LDI		R16, (1<< CLKPCE)
	STS		CLKPR, R16
	LDI		R16, (1<< CLKPS2)
	STS		CLKPR, R16

	// Iniciamos registros
	CLR		MODE
	CLR		ACTION

	// Llammamos para mostrar al inicio donde estamos
	CALL	LOAD_MODE_MENSAJE

	// Ponemos el mux en 0
	CLR		R16
	STS		MUX, R16

	// Iniciamos variables del reloj
	STS		SEGUNDOS, R16
	STS		MINUTOS, R16
	STS		PUNTO_CONT, R16
	STS		PUNTO_HALF, R16

	// Inicializamos hora predeterminada
	LDI		R16, 12
	STS		HORAS, R16

	// Inicializamos una fecha predeterminada
	LDI		R16, 10
	STS		DIA, R16
	LDI		R16, 3
	STS		MES, R16

		// Inicializamos alarma predeterminada
	LDI		R16, 6
	STS		AL_HORAS, R16

	CLR		R16
	STS		AL_MINUTOS, R16
	STS		AL_ACTIVA, R16

	// Iniciaos mensaje para mostrar al inicio
	LDI		R16, 1
	STS		MENSAJE, R16

	LDI		R16, LOW(1000)
	STS		MEN_LOW, R16
	LDI		R16, HIGH(1000)
	STS		MEN_HIGH, R16

	// Configuramos los botones con pullup
	CBI		DDRC, DDC2
	CBI		DDRC, DDC3
	CBI		DDRC, DDC4
	CBI		DDRC, DDC5

	SBI		PORTC, PORTC2
	SBI		PORTC, PORTC3
	SBI		PORTC, PORTC4
	SBI		PORTC, PORTC5

	CBI		DDRD, DDD7 // boton de modo
	SBI		PORTD, PORTD7

	// Configuramos los leds del display apagados
	SBI		DDRB, DDB0	// a
	SBI		DDRB, DDB1	// b
	SBI		DDRB, DDB2	// c
	SBI		DDRB, DDB3	// d
	SBI		DDRB, DDB4	// e
	SBI		DDRB, DDB5	// f
	SBI		DDRC, DDC0	// g
	SBI		DDRC, DDC1	// : o DP

	CBI		PORTB, PORTB0
	CBI		PORTB, PORTB1
	CBI		PORTB, PORTB2
	CBI		PORTB, PORTB3
	CBI		PORTB, PORTB4
	CBI		PORTB, PORTB5
	CBI		PORTC, PORTC0
	CBI		PORTC, PORTC1

	// Configuramos los bits de control
	SBI		DDRD, DDD2 // DIG 1
	SBI		DDRD, DDD3 // DIG 2
	SBI		DDRD, DDD4 // DIG 3
	SBI		DDRD, DDD5 // DIG 4

	CBI		PORTD, PORTD2
	CBI		PORTD, PORTD3
	CBI		PORTD, PORTD4
	CBI		PORTD, PORTD5

	// Configuramos la alarma
	SBI		DDRD, DDD6
	CBI		PORTD, PORTD6

	// Habilitamos las interrupciones para PCINT
	LDI		R16, (1 << PCIE1)|(1 << PCIE2)
	STS		PCICR, R16

	LDI		R16, (1<<PCINT10) | (1<<PCINT11) | (1<<PCINT12) | (1<<PCINT13)
	STS		PCMSK1, R16

	LDI		R16, (1<<PCINT23)
	STS		PCMSK2, R16

	// Llamamos a los timer
	CALL	INIT_TIMER0
	CALL	INIT_TIMER1

	// Guardamos el estado inicial del boton de modo
	IN		R16, PIND
	STS		PREV_D7, R16

	SEI // ENABLE GLOBAL INTERRUPCIONS

/****************************************/
// Loop Infinito
MAIN_LOOP:
	
	// Si hay interrupcion por pin change procesamos los botones
	SBRS	ACTION, FLAG_PCINT
	RJMP	NO_PCINT

	// Limpiamos la bandera
	CBR		ACTION, (1<<FLAG_PCINT)

	// Funciona solo cuando sono la alarma
	CPI		MODE, MODE_RING
	BRNE	REVISAR_MODO_NORMAL
	RJMP	REVISAR_RING

REVISAR_MODO_NORMAL:	
	// Revisamos el boton de modo
	CALL	CHECK_MODE_BUTTON
	RJMP	NO_PCINT

REVISAR_RING:
	CALL	CHECK_SILENCIO_RING

NO_PCINT:

	// Revisamos botones de ajuste
	CALL	CHECK_AJUSTES

	// Si Timer1 dio tick de 1 segundo, actualizamos reloj
	SBRS	ACTION, FLAG_TIMER_1
	RJMP	CHECK_ALARMA_LOOP

	CBR		ACTION, (1<<FLAG_TIMER_1)
	CALL	ACTUALIZAR_RELOJ
	CALL	RESET_ALARMA_CAMBIO_MINUTO

CHECK_ALARMA_LOOP:
	CALL	CHECK_ALARM
	CALL	CONTROL_RING
	CALL	CHECK_SILENCIO_RING

CHECK_MENSAJE:
	LDS		R16, MENSAJE
	CPI		R16, 1
	BREQ	SALTAR_ACTUALIZACION

	CALL	ACTUALIZAR_DISPLAY

SALTAR_ACTUALIZACION:
    RJMP    MAIN_LOOP


/****************************************/
// NON-Interrupt subroutines

// Delay pequeno para los botones
DELAY_REBOTE:
	LDI		R16, 25

REBOTE_LOOP1:
	LDI		R17, 120

REBOTE_LOOP2:
	DEC		R17
	BRNE	REBOTE_LOOP2

	DEC		R16
	BRNE	REBOTE_LOOP1
	RET


// Revisa que bloque de ajustes ejecutar
CHECK_AJUSTES:
	CPI		MODE, MODE_SET_CLOCK
	BRNE	REVISAR_SET_DATE
	RJMP	CHECK_SET_CLOCK

REVISAR_SET_DATE:
	CPI		MODE, MODE_SET_DATE
	BRNE	REVISAR_SET_ALARM
	RJMP	CHECK_SET_DATE

REVISAR_SET_ALARM:
	CPI		MODE, MODE_SET_ALARM
	BRNE	SALIR_CHECK_AJUSTES
	RJMP	CHECK_SET_ALARM

SALIR_CHECK_AJUSTES:
	RET


//	Revisa la hora actual coincide con la alarma puesta
CHECK_ALARM:

	LDS		R16, AL_ACTIVA
	CPI		R16, 1
	BREQ	SALIR_CHECK_ALARM

	LDS		R16, HORAS
	LDS		R17, AL_HORAS
	CP		R16, R17
	BRNE	SALIR_CHECK_ALARM

	LDS		R16, MINUTOS
	LDS		R17, AL_MINUTOS
	CP		R16, R17
	BRNE	SALIR_CHECK_ALARM

	LDS		R16, SEGUNDOS
	CPI		R16, 0
	BRNE	SALIR_CHECK_ALARM

	LDI		R16, 1
	STS		AL_ACTIVA, R16

	LDI		R16, MODE_RING
	MOV		MODE, R16

SALIR_CHECK_ALARM:
	RET


// Enciende o apaga el buzzer respecto a la alarma
CONTROL_RING:

	LDS		R16, AL_ACTIVA
	CPI		MODE, MODE_RING
	BRNE	APAGAR_RING

	// Encendemos buzzer
	SBI		PORTD, PORTD6
	RET

APAGAR_RING:

	// Apagamos buzzer
	CBI		PORTD, PORTD6
	RET


// Permite apagar el buzzer entrando al modo oculto de ring
CHECK_SILENCIO_RING:
	CPI		MODE, MODE_RING
	BRNE	SALIR_SILENCIO_RING

	SBIS	PIND, PIND7
	RJMP	CONFIRMAR_SILENCIO
	RET

CONFIRMAR_SILENCIO:
	CALL	DELAY_REBOTE
	SBIS	PIND, PIND7
	RJMP	APAGAR_ALARMA
	RET

APAGAR_ALARMA:
	
	CLR		R16
	STS		AL_ACTIVA, R16

	LDI		R16, MODE_CLOCK
	MOV		MODE, R16

	IN		R16, PIND
	STS		PREV_D7, R16

ESPERA_SUELTA_SILENCIO:
	SBIC	PIND, PIND7
	RJMP	SALIR_SILENCIO_RING
	RJMP	ESPERA_SUELTA_SILENCIO

SALIR_SILENCIO_RING:
	RET


// Reactiva la posibilidad de volver a prender la alarma
RESET_ALARMA_CAMBIO_MINUTO:
	LDS		R16, SEGUNDOS
	CPI		R16, 1
	BRNE	SALIR_RESET_ALARMA

	CLR		R16
	STS		AL_ACTIVA, R16

SALIR_RESET_ALARMA:
	RET


// Revisa los botones de ajustes en hora
CHECK_SET_CLOCK:
	SBIS	PINC, PINC2
	CALL	BOTON_INC_HORA

	SBIS	PINC, PINC3
	CALL	BOTON_DEC_HORA

	SBIS	PINC, PINC4
	CALL	BOTON_INC_MIN

	SBIS	PINC, PINC5
	CALL	BOTON_DEC_MIN

	RET


// Revisa los botones de ajustes en fecha
CHECK_SET_DATE:
	SBIS	PINC, PINC2
	CALL	BOTON_INC_DIA

	SBIS	PINC, PINC3
	CALL	BOTON_DEC_DIA

	SBIS	PINC, PINC4
	CALL	BOTON_INC_MES

	SBIS	PINC, PINC5
	CALL	BOTON_DEC_MES

	RET


// Revisa los botones que ajustan la alarma
CHECK_SET_ALARM:
	SBIS	PINC, PINC2
	CALL	BOTON_INC_AL_HORA

	SBIS	PINC, PINC3
	CALL	BOTON_DEC_AL_HORA

	SBIS	PINC, PINC4
	CALL	BOTON_INC_AL_MIN

	SBIS	PINC, PINC5
	CALL	BOTON_DEC_AL_MIN

	RET


// Permite incrementar la hora con antirebote
BOTON_INC_AL_HORA:
	CALL	DELAY_REBOTE
	SBIS	PINC, PINC2
	RJMP	CONFIRM_INC_AL_HORA
	RET

CONFIRM_INC_AL_HORA:
	CALL	INC_AL_HORAS

ESPERA_SUELTA_INC_AL_HORA:
	SBIC	PINC, PINC2
	RET
	RJMP	ESPERA_SUELTA_INC_AL_HORA


// Permite restar la hora con antirebote
BOTON_DEC_AL_HORA:
	CALL	DELAY_REBOTE
	SBIS	PINC, PINC3
	RJMP	CONFIRM_DEC_AL_HORA
	RET

CONFIRM_DEC_AL_HORA:
	CALL	DEC_AL_HORAS

ESPERA_SUELTA_DEC_AL_HORA:
	SBIC	PINC, PINC3
	RET
	RJMP	ESPERA_SUELTA_DEC_AL_HORA


// Permite incrementar los minutos con antirebote
BOTON_INC_AL_MIN:
	CALL	DELAY_REBOTE
	SBIS	PINC, PINC4
	RJMP	CONFIRM_INC_AL_MIN
	RET

CONFIRM_INC_AL_MIN:
	CALL	INC_AL_MINUTOS

ESPERA_SUELTA_INC_AL_MIN:
	SBIC	PINC, PINC4
	RET
	RJMP	ESPERA_SUELTA_INC_AL_MIN


// Permite restar los minutos con antirebote
BOTON_DEC_AL_MIN:
	CALL	DELAY_REBOTE
	SBIS	PINC, PINC5
	RJMP	CONFIRM_DEC_AL_MIN
	RET

CONFIRM_DEC_AL_MIN:
	CALL	DEC_AL_MINUTOS

ESPERA_SUELTA_DEC_AL_MIN:
	SBIC	PINC, PINC5
	RET
	RJMP	ESPERA_SUELTA_DEC_AL_MIN


// Incremento las horas con overflow
INC_AL_HORAS:
	LDS		R16, AL_HORAS
	INC		R16
	CPI		R16, 24
	BRNE	GUARDAR_INC_AL_HORAS

	CLR		R16

GUARDAR_INC_AL_HORAS:
	STS		AL_HORAS, R16
	RET

// Resta las horas con overflow
DEC_AL_HORAS:
	LDS		R16, AL_HORAS
	CPI		R16, 0
	BRNE	RESTAR_AL_HORA

	LDI		R16, 23
	STS		AL_HORAS, R16
	RET

RESTAR_AL_HORA:
	DEC		R16
	STS		AL_HORAS, R16
	RET


// INcremento los minutos con overflow
INC_AL_MINUTOS:
	LDS		R16, AL_MINUTOS
	INC		R16
	CPI		R16, 60
	BRNE	GUARDAR_INC_AL_MIN

	CLR		R16

GUARDAR_INC_AL_MIN:
	STS		AL_MINUTOS, R16
	RET


// Resta los minutos con underflow
DEC_AL_MINUTOS:
	LDS		R16, AL_MINUTOS
	CPI		R16, 0
	BRNE	RESTAR_AL_MIN

	LDI		R16, 59
	STS		AL_MINUTOS, R16
	RET

RESTAR_AL_MIN:
	DEC		R16
	STS		AL_MINUTOS, R16
	RET


// Boton para incrementar hora con antirebote
BOTON_INC_HORA:
	CALL	DELAY_REBOTE
	SBIS	PINC, PINC2
	RJMP	CONFIRM_INC_HORA
	RET

CONFIRM_INC_HORA:
	CALL	INC_HORAS

ESPERA_SUELTA_INC_HORA:
	SBIC	PINC, PINC2
	RET
	RJMP	ESPERA_SUELTA_INC_HORA


// Boton para resta hora con antirebote
BOTON_DEC_HORA:
	CALL	DELAY_REBOTE
	SBIS	PINC, PINC3
	RJMP	CONFIRM_DEC_HORA
	RET

CONFIRM_DEC_HORA:
	CALL	DEC_HORAS

ESPERA_SUELTA_DEC_HORA:
	SBIC	PINC, PINC3
	RET
	RJMP	ESPERA_SUELTA_DEC_HORA

// Boton para sumar minutos con antirebote
BOTON_INC_MIN:
	CALL	DELAY_REBOTE
	SBIS	PINC, PINC4
	RJMP	CONFIRM_INC_MIN
	RET

CONFIRM_INC_MIN:
	CALL	INC_MINUTOS

ESPERA_SUELTA_INC_MIN:
	SBIC	PINC, PINC4
	RET
	RJMP	ESPERA_SUELTA_INC_MIN


// Boton para restar minutos con antirebote
BOTON_DEC_MIN:
	CALL	DELAY_REBOTE
	SBIS	PINC, PINC5
	RJMP	CONFIRM_DEC_MIN
	RET

CONFIRM_DEC_MIN:
	CALL	DEC_MINUTOS

ESPERA_SUELTA_DEC_MIN:
	SBIC	PINC, PINC5
	RET
	RJMP	ESPERA_SUELTA_DEC_MIN


// Boton para sumar dia con antirebote
BOTON_INC_DIA:
	CALL	DELAY_REBOTE
	SBIS	PINC, PINC2
	RJMP	CONFIRM_INC_DIA
	RET

CONFIRM_INC_DIA:
	CALL	INC_DIA_EDIT

ESPERA_SUELTA_INC_DIA:
	SBIC	PINC, PINC2
	RET
	RJMP	ESPERA_SUELTA_INC_DIA


// Boton para restar dia con antirebote
BOTON_DEC_DIA:
	CALL	DELAY_REBOTE
	SBIS	PINC, PINC3
	RJMP	CONFIRM_DEC_DIA
	RET

CONFIRM_DEC_DIA:
	CALL	DEC_DIA_EDIT

ESPERA_SUELTA_DEC_DIA:
	SBIC	PINC, PINC3
	RET
	RJMP	ESPERA_SUELTA_DEC_DIA


// Boton para sumar mes con antirebote
BOTON_INC_MES:
	CALL	DELAY_REBOTE
	SBIS	PINC, PINC4
	RJMP	CONFIRM_INC_MES
	RET

CONFIRM_INC_MES:
	CALL	INC_MES_EDIT

ESPERA_SUELTA_INC_MES:
	SBIC	PINC, PINC4
	RET
	RJMP	ESPERA_SUELTA_INC_MES


// Boton para restar mes con antirebote
BOTON_DEC_MES:
	CALL	DELAY_REBOTE
	SBIS	PINC, PINC5
	RJMP	CONFIRM_DEC_MES
	RET

CONFIRM_DEC_MES:
	CALL	DEC_MES_EDIT

ESPERA_SUELTA_DEC_MES:
	SBIC	PINC, PINC5
	RET
	RJMP	ESPERA_SUELTA_DEC_MES


// Incrementa la hora del reloj con overflow
INC_HORAS:
	LDS		R16, HORAS
	INC		R16
	CPI		R16, 24
	BRNE	GUARDAR_INC_HORAS

	CLR		R16

GUARDAR_INC_HORAS:
	STS		HORAS, R16
	RET


// REsta la hora del reloj con underflow
DEC_HORAS:
	LDS		R16, HORAS
	CPI		R16, 0
	BRNE	RESTAR_HORA

	LDI		R16, 23
	STS		HORAS, R16
	RET

RESTAR_HORA:
	DEC		R16
	STS		HORAS, R16
	RET


// Incrementa los minutos del reloj con overflow
INC_MINUTOS:
	LDS		R16, MINUTOS
	INC		R16
	CPI		R16, 60
	BRNE	GUARDAR_INC_MIN

	CLR		R16

GUARDAR_INC_MIN:
	STS		MINUTOS, R16
	RET

// Resta los minutos del reloj con underflow
DEC_MINUTOS:
	LDS		R16, MINUTOS
	CPI		R16, 0
	BRNE	RESTAR_MIN

	LDI		R16, 59
	STS		MINUTOS, R16
	RET

RESTAR_MIN:
	DEC		R16
	STS		MINUTOS, R16
	RET


// Incrementa el dia de mes respetando la cantidad en cada mes
INC_DIA_EDIT:
	LDS		R18, DIA
	INC		R18

	CALL	OBTENER_MAX_DIA		// R16 = maximo dia del mes actual
	CP		R18, R16
	BRLO	GUARDAR_DIA_INC
	BREQ	GUARDAR_DIA_INC

	LDI		R18, 1

GUARDAR_DIA_INC:
	STS		DIA, R18
	RET


// Resta el dia del mes respetando la cantidad de cada mes
DEC_DIA_EDIT:
	LDS		R18, DIA
	CPI		R18, 1
	BRNE	RESTAR_DIA_EDIT

	CALL	OBTENER_MAX_DIA		// R16 = maximo dia del mes actual
	STS		DIA, R16
	RET

RESTAR_DIA_EDIT:
	DEC		R18
	STS		DIA, R17
	RET


// Incrementa el mes con overflow
INC_MES_EDIT:
	LDS		R16, MES
	INC		R16
	CPI		R16, 13
	BRNE	GUARDAR_INC_MES_EDIT

	LDI		R16, 1

GUARDAR_INC_MES_EDIT:
	STS		MES, R16
	CALL	AJUSTAR_DIA_VALIDO
	RET


// Decrementa el mes con underflow
DEC_MES_EDIT:
	LDS		R16, MES
	CPI		R16, 1
	BRNE	RESTAR_MES_EDIT

	LDI		R16, 12
	STS		MES, R16
	CALL	AJUSTAR_DIA_VALIDO
	RET

RESTAR_MES_EDIT:
	DEC		R16
	STS		MES, R16
	CALL	AJUSTAR_DIA_VALIDO
	RET


// Devuelve en R16 el valor maximo de dia en cada mes
OBTENER_MAX_DIA:
	LDS		R17, MES

	CPI		R17, 2
	BREQ	MAX_FEBRERO

	CPI		R17, 4
	BREQ	MAX_30
	CPI		R17, 6
	BREQ	MAX_30
	CPI		R17, 9
	BREQ	MAX_30
	CPI		R17, 11
	BREQ	MAX_30

	LDI		R16, 31
	RET

MAX_FEBRERO:
	LDI		R16, 28
	RET

MAX_30:
	LDI		R16, 30
	RET


// Corrige el dia actual si excede el madimos del mes
AJUSTAR_DIA_VALIDO:
	CALL	OBTENER_MAX_DIA
	MOV		R18, R16

	LDS		R16, DIA
	CP		R16, R18
	BREQ	SALIR_AJUSTE_DIA
	BRLO	SALIR_AJUSTE_DIA

	STS		DIA, R18

SALIR_AJUSTE_DIA:
	RET

CHECK_MODE_BUTTON:
	
	IN		R16, PIND // Leemos el estado actual
	LDS		R17, PREV_D7 // Cargamos el estado anterior

	SBRS	R17, 7
	RJMP	GUARDAR_SALIR // Si ya era 0, no cambio

	SBRS	R16, 7
	RJMP	CONFIRMAR_MODO // Revisamos si el bit 7 es 0

	RJMP	GUARDAR_SALIR


// Confirma que el modo fue realmente presionado
CONFIRMAR_MODO:
	CALL	DELAY_REBOTE
	SBIS	PIND, PIND7
	RJMP	HACER_CAMBIO_MODO
	RJMP	GUARDAR_SALIR

HACER_CAMBIO_MODO:
	CALL	CHANGE_MODE

ESPERA_SUELTA_MODO:
	SBIC	PIND, PIND7
	RJMP	GUARDAR_SALIR
	RJMP	ESPERA_SUELTA_MODO

GUARDAR_SALIR:
	IN		R16, PIND
	STS		PREV_D7, R16 // Guardamos la actual como anterior
	RET


// Avanza al siguiente modo y reinicia el mensaje temporal
CHANGE_MODE:
	INC		MODE
	CPI		MODE, MAX_MODES // Compara si ya llego al último

	BRNE	NO_RESET_MODE
	CLR		MODE

NO_RESET_MODE:
	CALL	LOAD_MODE_MENSAJE // Muestra el estado

	LDI		R16, 1
	STS		MENSAJE, R16

	LDI		R16, LOW(1000)
	STS		MEN_LOW, R16
	LDI		R16, HIGH(1000)
	STS		MEN_HIGH, R16

	RET
