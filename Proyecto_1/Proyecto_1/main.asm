	PUSH	R16
	IN		R16, SREG
	PUSH	R16

	SBR		ACTION, (1<<FLAG_PCINT)

	POP		R16
	OUT		SREG, R16
	POP		R16
	RETI

// Marca el tick de un segundo
TIMER1_ISR:
	PUSH	R16
	IN		R16, SREG
	PUSH	R16

	SBR		ACTION, (1<<FLAG_TIMER_1)

	POP		R16
	OUT		SREG, R16
	POP		R16
	RETI

// Marca el tick de 1 ms
TIMER0_ISR:
	PUSH	R16
	PUSH	R17
	PUSH	R18

	IN		R16, SREG
	PUSH	R16

	CALL	APAGAR_DIGITOS
	CALL	APAGAR_SEGMENTOS
	NOP
	NOP
	CALL	MUX_DISPLAY

	// Parpadeo de puntos
	LDS		R16, PUNTO_CONT
	INC		R16
	STS		PUNTO_CONT, R16

	CPI		R16, 250
	BRNE	FIN_PUNTO

	CLR		R16
	STS		PUNTO_CONT, R16

	LDS		R16, PUNTO_HALF
	INC		R16
	STS		PUNTO_HALF, R16

	CPI		R16, 2
	BRNE	FIN_PUNTO

	CLR		R16
	STS		PUNTO_HALF, R16

	SBIS	PORTC, PORTC1
	RJMP	ENCENDER_PUNTO

	CBI		PORTC, PORTC1
	RJMP	FIN_PUNTO

// Enciende ":"
ENCENDER_PUNTO:
	SBI		PORTC, PORTC1

FIN_PUNTO:
	// Mensaje por 1 segundo
	LDS		R16, MENSAJE
	CPI		R16, 1
	BRNE	FIN_TIMER0

	LDS		R16, MEN_LOW
	SUBI	R16, 1
	STS		MEN_LOW, R16

	LDS		R17, MEN_HIGH
	SBCI	R17, 0
	STS		MEN_HIGH, R17

	LDS		R16, MEN_LOW
	LDS		R17, MEN_HIGH
	OR		R16, R17
	BRNE	FIN_TIMER0

	CLR		R16
	STS		MENSAJE, R16

FIN_TIMER0:

	POP		R16
	OUT		SREG, R16

	POP		R18
	POP		R17
	POP		R16
	RETI

