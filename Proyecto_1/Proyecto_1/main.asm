	STS		DISP0, R16

	LDI		R16, SEG_A
	STS		DISP1, R16

	LDI		R16, SEG_t
	STS		DISP2, R16

	LDI		R16, SEG_E
	STS		DISP3, R16

	RET

	// Convierte y carga la hora d ela alarma en el display
MOSTRAR_ALARMA:
