	BREQ	NUM3
	CPI		R16, 4
	BREQ	NUM4
	CPI		R16, 5
	BREQ	NUM5
	CPI		R16, 6
	BREQ	NUM6
	CPI		R16, 7
	BREQ	NUM7
	CPI		R16, 8
	BREQ	NUM8
	CPI		R16, 9
	BREQ	NUM9
	RET

