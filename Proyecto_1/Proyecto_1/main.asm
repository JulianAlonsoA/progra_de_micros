ACTUALIZAR_RELOJ:
	
	// Segundos
	LDS		R16, SEGUNDOS
	INC		R16
	STS		SEGUNDOS, R16

	CPI		R16, 60
	BRNE	EXIT_RELOJ
	 
	CLR		R16
	STS		SEGUNDOS, R16

	// Minutos
	LDS		R16, MINUTOS
	INC		R16
	STS		MINUTOS, R16

	CPI		R16, 60
	BRNE	EXIT_RELOJ

	CLR		R16
	STS		MINUTOS, R16

	// Horas
	LDS		R16, HORAS
	INC		R16
	STS		HORAS, R16

	CPI		R16, 24
	BRNE	EXIT_RELOJ

	CLR		R16
	STS		HORAS, R16

	CALL	ACTUALIZAR_FECHA
EXIT_RELOJ:
	RET

// Actualiza todo lo de modo de fecha
ACTUALIZAR_FECHA:
	LDS		R16, DIA
	INC		R16
	STS		DIA, R16

	LDS		R17, MES
	// Revisamos si es febrero
	CPI		R17, 2
	BREQ	MES_FEBRERO

	// Revisamos si son de 30 dias
	CPI		R17, 4
	BREQ	MES_30
	CPI		R17, 6
	BREQ	MES_30
	CPI		R17, 9
	BREQ	MES_30
	CPI		R17, 11
	BREQ	MES_30

	// resto son de 31
	RJMP	MES_31

MES_FEBRERO:
	LDS		R16, DIA
	CPI		R16, 29

	BRNE	SALIR_FECHA

	LDI		R16, 1
	STS		DIA, R16
	RJMP	AVANZAR_MES

MES_30:
	LDS		R16, DIA
	CPI		R16, 31

	BRNE	SALIR_FECHA

	LDI		R16, 1
	STS		DIA, R16
	RJMP	AVANZAR_MES

MES_31:
	LDS		R16, DIA
	CPI		R16, 32

	BRNE	SALIR_FECHA

	LDI		R16, 1
	STS		DIA, R16
	RJMP	AVANZAR_MES

AVANZAR_MES:
	LDS		R16, MES
	INC		R16
	STS		MES, R16

	CPI		R16, 13
	BRNE	SALIR_FECHA

	LDI		R16, 1
	STS		MES, R16

SALIR_FECHA:
	RET

// Carga el mensaje de "date"
MOSTRAR_DATE:

