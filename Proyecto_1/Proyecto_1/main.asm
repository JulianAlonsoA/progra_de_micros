	RET
NUM2:
	LDI		R16, SEG_2
	RET
NUM3:
	LDI		R16, SEG_3
	RET
NUM4:
	LDI		R16, SEG_4
	RET
NUM5:
	LDI		R16, SEG_5
	RET
NUM6:
	LDI		R16, SEG_6
	RET
