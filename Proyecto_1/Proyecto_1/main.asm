	BRLO	FIN_DEC_AL_HORA
	SUBI	R16, 10
	INC		R17
	RJMP	DEC_AL_HORA

FIN_DEC_AL_HORA:
	
	MOV		R18, R16
	MOV		R16, R17

	CALL	NUM_A_7SEG
	STS		DISP0, R16

	MOV		R16, R18
	CALL	NUM_A_7SEG
	STS		DISP1, R16

	LDS		R16, AL_MINUTOS
	CLR		R17

DEC_AL_MIN:
	
	CPI		R16, 10
	BRLO	FIN_DEC_AL_MIN
	SUBI	R16, 10
	INC		R17
	RJMP	DEC_AL_MIN

FIN_DEC_AL_MIN:
	
	MOV		R18, R16
	MOV		R16, R17

	CALL	NUM_A_7SEG
	STS		DISP2, R16

	MOV		R16, R18
	CALL	NUM_A_7SEG
	STS		DISP3, R16

	RET
