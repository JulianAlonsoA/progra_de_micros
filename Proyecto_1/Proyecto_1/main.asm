	LDI		R16, SEG_9
	RET

APAGAR_DIGITOS:

	// Apagamos para evitar ghosting
	CBI		PORTD, PORTD2 // DIG1
	CBI		PORTD, PORTD3 // DIG2
	CBI		PORTD, PORTD4 // DIG3
	CBI		PORTD, PORTD5 // DIG4
	RET

CARGAR_SEGMENTOS:

	MOV		R17, R16

	// Segmento a
	SBRS	R17, 0
	CBI		PORTB, PORTB0
	SBRC	R17, 0
	SBI		PORTB, PORTB0

	// Segmento b
	SBRS	R17, 1
	CBI		PORTB, PORTB1
	SBRC	R17, 1
	SBI		PORTB, PORTB1

	// Segmento c
	SBRS	R17, 2
	CBI		PORTB, PORTB2
	SBRC	R17, 2
	SBI		PORTB, PORTB2

	// Segmento d
	SBRS	R17, 3
	CBI		PORTB, PORTB3
	SBRC	R17, 3
	SBI		PORTB, PORTB3

	// Segmento e
	SBRS	R17, 4
	CBI		PORTB, PORTB4
	SBRC	R17, 4
	SBI		PORTB, PORTB4

	// Segmento f
	SBRS	R17, 5
	CBI		PORTB, PORTB5
	SBRC	R17, 5
	SBI		PORTB, PORTB5

	// Segmento g
	SBRS	R17, 6
	CBI		PORTC, PORTC0
	SBRC	R17, 6
	SBI		PORTC, PORTC0

	RET


// Multiplexa un digito del display en cada call
MUX_DISPLAY:
	
	LDS		R18, MUX

	CPI		R18, 0
	BREQ	DIG0

	CPI		R18, 1
	BREQ	DIG1

	CPI		R18, 2
	BREQ	DIG2

	RJMP	DIG3

DIG0:
	LDS		R16, DISP3
	CALL	CARGAR_SEGMENTOS
	SBI		PORTD, PORTD2
	RJMP	SIGUIENTE_DIG

DIG1:
	LDS		R16, DISP2
	CALL	CARGAR_SEGMENTOS
	SBI		PORTD, PORTD3
	RJMP	SIGUIENTE_DIG

DIG2:
	LDS		R16, DISP1
	CALL	CARGAR_SEGMENTOS
	SBI		PORTD, PORTD4
	RJMP	SIGUIENTE_DIG

DIG3:
	LDS		R16, DISP0
	CALL	CARGAR_SEGMENTOS
	SBI		PORTD, PORTD5


// Avanza al siguiente digito
SIGUIENTE_DIG:
	INC		R18
	CPI		R18, 4
	BRNE	GUARDAR_MUX

	CLR		R18


// Guardar el indice actual
GUARDAR_MUX:
	STS		MUX, R18
	RET


// Apaga segmentos antes del siguiente
APAGAR_SEGMENTOS:

	CBI		PORTB, PORTB0
	CBI		PORTB, PORTB1
	CBI		PORTB, PORTB2
	CBI		PORTB, PORTB3
	CBI		PORTB, PORTB4
	CBI		PORTB, PORTB5
	CBI		PORTC, PORTC0
	RET

MOSTRAR_HORA:
	// Decenas y unidades de hora
	LDS		R16, HORAS
	CLR		R17

// Calcula la decena de hroas
DEC_HORA:
	CPI		R16, 10
	BRLO	FIN_DEC_HORA
	SUBI	R16, 10
	INC		R17
	RJMP	DEC_HORA

FIN_DEC_HORA:
	MOV		R18, R16
	MOV		R16, R17

	CALL	NUM_A_7SEG
	STS		DISP0, R16

	MOV		R16, R18
	CALL	NUM_A_7SEG
	STS		DISP1, R16

	// Decenas y unidades de minuto
	LDS		R16, MINUTOS
	CLR		R17

// Calcula la decena de minutos
DEC_MIN:	
